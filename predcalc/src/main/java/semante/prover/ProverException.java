package semante.prover;

public class ProverException extends Exception {

	private static final long serialVersionUID = -8390072516905856453L;

	public ProverException(String arg0) {
		super(arg0);
	}

	public ProverException(Throwable arg0) {
		super(arg0);
	}

	public ProverException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
